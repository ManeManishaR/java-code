package incapsulation;

public class Encapsulation {

	private int empid;
	private String empname;
	private int empage;
	//creating gatters and setters method.
	public int getId() {
		return empid;
	}
	public void setId(int id) {
		this.empid = id;
	}
	public String getName() {
		return empname;
	}
	public void setName(String name) {
		this.empname = name;
	}
	public int getAge() {
		return empage;
	}
	public void setAge(int age) {
		this.empage = age;
	}
	
	}

